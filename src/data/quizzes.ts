export const getQuizByCategory = (cat: 'basics'|'tools'|'glasses'|'spirits'|'techniques'|'cocktails'|'service'|'mixed', count = 5): typeof QUIZZES => {
  const pool = QUIZZES.filter(q => q.category === cat || cat === 'mixed')
  return pool.sort(() => Math.random() - 0.5).slice(0, count)
}
